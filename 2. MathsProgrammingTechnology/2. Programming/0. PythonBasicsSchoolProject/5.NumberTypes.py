# Natural numbers (1, 2, 3, 4 ... )
# Whole numbers (0, 1, 2, 3, 4 ... )
# Integers ( ... -4, -3, -2, -1, 0, 1, 2, 3, 4 ... )
print(3, 45, -9, +0, 0, -0)

# Integers (1, 34, 56, -76)
# Fractions (3/4, 7/4, -9/8, ...)
# Rational/Floating point numbers (2.34, -4.5, -6.56...)
print(3/4, 5/7, 11/8)

# Irrational numbers are cast to rational numbers
print(2**0.5)

# Complex numbers   -- Examples: Spatial rotations are easier to do using complex numbers
complex(3,4)
complex(3,4)+complex(4,5)